class Person:

    def __init__(self,name,surname,emailid,year_of_birth):
        self.name = name
        self.surname = surname
        self.emailid = emailid
        self.year_of_birth = year_of_birth



omkar_var = Person("omkar","pomendkar","omkarpomendkar100@gmail.com",2000)
print(omkar_var.name)
print(omkar_var.surname)
print(omkar_var.emailid)
print(omkar_var.year_of_birth)

